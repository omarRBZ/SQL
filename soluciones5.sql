SELECT EMPLOYEE_ID , LAST_NAME ,FIRST_NAME, SALARY,TRUNC(MONTHS_BETWEEN(SYSDATE,HIRE_DATE)/12)
FROM EMPLOYEES
WHERE SALARY > (SELECT TRUNC( AVG(SALARY))
                FROM EMPLOYEES)
AND TRUNC(MONTHS_BETWEEN(SYSDATE,HIRE_DATE)/12) <10              



