SELECT E.EMPLOYEE_ID, E.LAST_NAME,
        D.DEPARTMENT_ID, D.DEPARTMENT_NAME
FROM EMPLOYEES E LEFT JOIN  DEPARMENTS D
ON  E.DEPARMENT_ID=D.DEPARMENT_ID
ORDER BY E.EMPLOYEE_ID
--RIGHT JOIN 
SELECT E.EMPLOYEE_ID, E.LAST_NAME,
        D.DEPARTMENT_ID, D.DEPARTMENT_NAME
FROM EMPLOYEES E RIGHT JOIN  DEPARMENTS D
ON  E.DEPARMENT_ID=D.DEPARMENT_ID
ORDER BY E.EMPLOYEE_ID
-- FULL JOIN 
SELECT E.EMPLOYEE_ID, E.LAST_NAME,
        D.DEPARTMENT_ID, D.DEPARTMENT_NAME
FROM EMPLOYEES E FULL JOIN  DEPARMENTS D
ON  E.DEPARMENT_ID=D.DEPARMENT_ID
ORDER BY E.EMPLOYEE_ID



