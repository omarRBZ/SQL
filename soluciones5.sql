-->LEFT OUTER JOIN  PERMITEN QUE SIN QUE LA CONDICION NO ESTA DECLARADA PUEDE SER MOSTRADA SE REPRESENTA CON (+)
SELECT E.EMPLOYEE_ID,E.LAST_NAME,
        D.DEPARTMENT_ID,D.DEPARTMENT_NAME,
        L.LOCATION_ID ,L.STREET_ADDRESS
FROM EMPLOYEES E , DEPARMENTS D, LOCATIONS L
WHERE E.DEPARMENT_ID =D.DEPARMENT_ID(+)
AND   D.LOCATION_ID = L.LOCATION_ID
ORDER BY E.EMPLOYEE_ID 

-->RIGTH OUTER JOIN SOLO LOS DE LA DERECHA SE REPRESENTA 
SELECT E.EMPLOYEE_ID,E.LAST_NAME,
        D.DEPARTMENT_ID,D.DEPARTMENT_NAME,
        L.LOCATION_ID ,L.STREET_ADDRESS
