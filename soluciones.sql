SELECT P.NOMBRE ,E.NOMBRE ,MSPP.MASKED
FROM (SELECT A.ID_PROYECTOS , MAX(E.SALARIO) MS 
      FROM ASIGNACIONES A 
      JOIN EMPLEADOS E 
      ON A.ID_EMPLEADO=E.ID
      GROUP BY ID_PROYECTO) MSPP
JOIN ASIGNACIONES A 
ON MSPP.ID_PROYECTO = A.ID_PROYECTO
AND E.SALARIO=MSPP.MS 
RIGHT JOIN PROYECTOS P 
ON P.ID=A.ID_PROYECTO
ORDER BY P.NOMBRE 





