SELECT D.NOMBRE ,NVL(NPPD.NP,0) NUM_PROYECTOS
FROM DEPARTAMENTOS D 
LEFT JOIN (SELECT E.ID ,COUNT(DISTINCT A.ID_PROYECTO)NP
           FROM ASIGNACIONES A 
           JOIN EMPLEDOS E 
           ON A.ID_EMPLEADO=E.ID
           GROUP BY E.ID_DEPARTAMENTO)NPPD
ON D.ID=NPPD.ID_DEPARTAMENTO
ORDER BY D.NOMBRE 

