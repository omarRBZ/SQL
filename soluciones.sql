SELECT P.NOMBRE NVL(CPP.C,0) COSTO 
FROM PROYECTOS 
LEFT JOIN (SELECT A.ID_PROYECTO ,SUM(E.SALARIO)
           FROM ASIGNACIONES A 
           JOIN EMPLEADOS E 
           ON A.ID_EMPLEADO=E.ID
           GROUP BY ID_PROYECTO)CPP
ON P.ID=CPP.ID_PROYECTO
ORDER BY P.NOMBRE 

